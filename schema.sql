create table if not exists academias (
  id bigint generated by default as identity primary key,
  nome text not null,
  telefone text,
  endereco text,
  responsavel text,
  logo_url text,
  created_at timestamptz default now()
);

create table if not exists perfis (
  id bigint generated by default as identity primary key,
  nome text unique not null,
  permissoes jsonb default '[]'::jsonb
);

create table if not exists usuarios (
  id bigint generated by default as identity primary key,
  nome text not null,
  usuario text unique not null,
  senha text not null,
  perfil text not null default 'aluno',
  aluno_id bigint,
  ativo boolean default true,
  created_at timestamptz default now()
);

create table if not exists professores (
  id bigint generated by default as identity primary key,
  nome text not null,
  telefone text,
  email text,
  especialidade text,
  cref text,
  ativo boolean default true,
  created_at timestamptz default now()
);

create table if not exists alunos (
  id bigint generated by default as identity primary key,
  nome text not null,
  telefone text,
  email text,
  objetivo text,
  nivel text default 'Iniciante',
  peso numeric,
  altura numeric,
  professor_id bigint references professores(id),
  nutricao text,
  restricoes text,
  ativo boolean default true,
  created_at timestamptz default now()
);

alter table usuarios add column if not exists aluno_id bigint;
alter table alunos add column if not exists nivel text default 'Iniciante';
alter table alunos add column if not exists restricoes text;
alter table professores add column if not exists cref text;

do $$ begin
  alter table usuarios add constraint usuarios_aluno_id_fkey foreign key (aluno_id) references alunos(id) on delete set null;
exception when duplicate_object then null;
end $$;

create table if not exists exercicios (
  id bigint generated by default as identity primary key,
  nome text not null,
  grupo text,
  descricao text,
  video_url text,
  youtube_url text,
  ativo boolean default true,
  created_at timestamptz default now()
);

create table if not exists treinos (
  id bigint generated by default as identity primary key,
  nome text not null,
  tipo text,
  divisao text,
  objetivo text,
  indicacao text,
  ativo boolean default true,
  created_at timestamptz default now()
);

create table if not exists treino_itens (
  id bigint generated by default as identity primary key,
  treino_id bigint references treinos(id) on delete cascade,
  exercicio_id bigint references exercicios(id),
  series text,
  repeticoes text,
  carga text,
  descanso text,
  observacao text,
  ordem int default 0
);

create table if not exists atribuicoes_treino (
  id bigint generated by default as identity primary key,
  aluno_id bigint references alunos(id) on delete cascade,
  treino_id bigint references treinos(id),
  professor_id bigint references professores(id),
  data_inicio date default current_date,
  data_fim date,
  status text default 'Ativo',
  observacao text
);

create table if not exists execucoes_treino (
  id bigint generated by default as identity primary key,
  aluno_id bigint references alunos(id),
  treino_id bigint references treinos(id),
  data date default current_date,
  inicio timestamptz default now(),
  fim timestamptz,
  status text default 'Em andamento',
  observacao text
);

create table if not exists evolucoes_aluno (
  id bigint generated by default as identity primary key,
  aluno_id bigint references alunos(id) on delete cascade,
  data date default current_date,
  peso numeric,
  massa text,
  gordura text,
  cintura text,
  quadril text,
  peito text,
  braco text,
  observacao text
);

create table if not exists indicacoes_nutricionais (
  id bigint generated by default as identity primary key,
  aluno_id bigint references alunos(id) on delete cascade,
  data date default current_date,
  objetivo text,
  descricao text,
  calorias text,
  proteina text,
  agua text,
  observacao text
);

create index if not exists idx_alunos_nome on alunos using gin (to_tsvector('portuguese', coalesce(nome,'')));
create index if not exists idx_exercicios_nome on exercicios using gin (to_tsvector('portuguese', coalesce(nome,'')));
create index if not exists idx_treinos_nome on treinos using gin (to_tsvector('portuguese', coalesce(nome,'')));

